#!/usr/bin/env python3
"""Простейшая программа для знакомства с программированием.

Программа приветствует пользователя, спрашивает имя и просит ввести два
числа, после чего выводит их сумму.
"""


def main() -> None:
    """Точка входа в программу."""
    print("Привет! Это простая программа.")

    # Спрашиваем имя пользователя
    name = input("Как тебя зовут? ")
    print(f"Приятно познакомиться, {name}!")

    # Запрашиваем два числа и выводим их сумму
    try:
        first = int(input("Введите первое число: "))
        second = int(input("Введите второе число: "))
    except ValueError:
        print("Нужно вводить именно целые числа.")
        return

    print(f"{first} + {second} равно {first + second}")


if __name__ == "__main__":
    main()
